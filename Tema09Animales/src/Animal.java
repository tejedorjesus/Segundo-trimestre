
public class Animal {
	
	
	
	
	private int numeroDeCrias=0;
	public Animal( int numeroDeCrias) {
		
		
		this.numeroDeCrias = numeroDeCrias;
	}
	
	

	
	public int getNumeroDeCrias() {
		return numeroDeCrias;
	}
	public void setNumeroDeCrias(int numeroDeCrias) {
		this.numeroDeCrias = numeroDeCrias;
	}
	
	

}
